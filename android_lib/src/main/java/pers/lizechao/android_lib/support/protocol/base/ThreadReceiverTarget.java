@Target({ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
public @interface ThreadReceiverTarget {
    enum ThreadTarget {
        AndroidMain,
        Io,
        Computation,
        NewThread
    }
    ThreadTarget value();
}
